export function translate(lang, key, vars) {
  const table = UI[lang] || UI.en;
  let str = table[key];
  if (str == null) str = UI.en[key];
  if (str == null) return key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.split(`{${k}}`).join(String(v));
    }
  }
  return str;
}
